package domain;

public class Circle {
    // attributes
    private double radio;
    
    // constructors
    public Circle() {};
    
    public Circle(double radio) {
        this.radio = radio;
    }
    
    // getters & setters
    public double getRadio() {
        return radio;
    }

    public void setRadio(double radio) {
        this.radio = radio;
    }
    
}
